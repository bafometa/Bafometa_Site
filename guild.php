<div id="zvan-zach"><h4>Зачистки</h4>
<span>Каков порядок исключения и разжалования игроков?</span><br>
В «Синих Ходоках» не принято гнаться за количеством игроков в гильдии, мы предпочитаем качество, соответственно, у нас регулярно проводятся зачистки игроков, которые отсутствуют длительное время.<br>
<span>Новичок</span> не достигший максимального уровня исключается при отсутствии более 6-7 дней. Рекруты, достигшие максимального уровня исключаются при отсутствии 14 дней, однако, если у игрока отсутствует заметка, то исключение произойдет уже через 7 дней отсутствия.<br>
<span>Рекрут</span> исключаются при несогласованном отсутствии от одного до двух месяцев.<br>
<span>Рейдеры</span> в случае согласованного (предупреждение на форуме) отсутствия не исключаются из гильдии, а переводятся в звание Друзья. В случае несогласованного отсутствия Рейдер подлежит разжалованию в звание Новичок с соответствующими сроками на отсутствие. Также Рейдеры теряют звание в случае, если снимаются с должности, которая подразумеваем звание - в этом случае также, в зависимости от успехов на посту, экс-Рейдера ждет либо звание Друзья, либо звание Новичка.<br>
<span>Совет Гильдии</span> и выше не подлежат исключению никогда, производятся в Друзья на "пенсию" и торчат в списке гильдии хоть годами. Однако, если звание дано авансом (пришел РЛ, но не стал РЛить), член Совета Гильдии может быть разжалован в любое нижестоящее звание с последующими сложностями по сохранению за собой места в гильдии.</div>

<h4>Субординация</h4>
<span>Кто мой начальник? К кому обращаться за справедливостью?</span><br>
В нашей гильдии поддерживается дисциплина не только внутри статиков, но и в других аспектах игрового процесса, поэтому важно осознавать порядок субординации и знать "в лицо" своих руководителей.<br>
Во главе всей гильдии стоит <span>Генерал</span> - он назначает Рейд Лидеров, Класс Лидеров и игроков, занимающихся администрированием, рекрутингом, сопровождением новичков, работой с сайтом гильдии и т.д. Генерал - глава гильдии, поддерживает работоспособность направлений и регулирует отношения между РейдЛидерами и руководителями внутри статиков, соответственно, именно он задает вектор и действует в интересах развития гильдии.<br>
Заместителями Генерала в деятельности развития гильдии являются <span>члены Совет Гильдии на должности Лидеров PvE-направления и PvP-направления</span>. В их обязанности входит регулирование всего, связанного с конкретным "крылом" гильдии, например, поиск РЛов и помощь им, организация новых статиков или их расформирование (если это необходимо для развития основных составов). Они распоряжаются назначением игроков по статикам и регулированием отношений между РейдЛидерами.<br>
Следом за Лидерами направлений, идут <span>РейдЛидеры</span>. РЛы в гильдии бывают разные: некоторые стоят во главе статиков, некотрые занимаются организацией нестатичных, но регулярных ивентов. РЛы распоряжаются игроками своих составов и управлением всем, происходящем в рейдах. ТОЛЬКО РЛы статиков назначают Рекрутов/Рейдеров (статичных игроков) и Друзей (командиров групп, помощников по администрированию статиков). Во время проведения рейда/рбг/другого ивента РейдЛидер - царь и бог. ТОЛЬКО он принимает окончательные решения и оспариваться они могут только после РТ, даже Лидеры направлений или Генерал не имеют права перечить РЛу во время рейда. <br>
<span>Рейдеры</span> - это, зачастую, игроки статика. Они не распоряжаются другими игроками, а подчиняются напрямую РЛам своего статика.<br>
Игроки, в звании <span>Друзья</span> или <span>Совета Гильдии</span>, занимающие НЕ игровые должности (модераторы форума, видеомейкеры, помощники по администрированию гильдии) не распоряжаются игроками напрямую. Обо всех игроках на руководящих постах вы можете прочесть на <a href="http://bluewallkers.topbb.ru/pages/roster">странице Ростера гильдии</a>.<br>
