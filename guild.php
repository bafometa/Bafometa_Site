<b>PvE, PvP, PvA, RP?</b><br>
Гильдия «Синие Ходоки» не зацикливается на одном аспекте игры, и сочетает в себе и PvE, PvP и Ачив-раны.<br>
Каждый из членов Совета Гильдии отвечает за свое направление, и развитие гильдии в том или ином аспекте зависит от игровой активности руководящего состава на данный момент. В связи с этим, бывают моменты спада активности в той или иной деятельности, но мы никогда не зацикливались на чем-то одном и не ограничивали аудиторию гильдии сугубо одним статиком.<br>
<br>
<b>Статики</b><br>
Что касается прогресса PvE/PvP, мы хотим создать несколько статиков с разными требованиями к игрокам и под руководством разных РейдЛидеров.<br>
Актуальная информация о действующих на данный момент статиках и требованиях для вступления в них размещается на странице <a href="http://bluewallkers.topbb.ru/pages/rekruting">Рекрутинга</a>.<br>
Для статиков в гильдии создана развитая экономическая система - Рейдеры, играющие на прогресс, обеспечиваются едой, настоями, зельями, всевозможными чантами и камнями. Также особо перспективным и стабильным игрокам выделяется голд для покупки вещей или крайфта. <br>
<br>
<b>Обучение новичков</b><br>
Наша гильдия рада не только матерым убиваторам, но и новичкам в мире World of Warcraft. <br>
При прогрессивных статиках мы стараемся организовать более казуальные статики или организуем еженедельные походы на более низкой сложности/на более низком рейтинге, давая возможность опробовать себя начинающим игрокам. А для тех, кто стремиться прогрессировать, мы предоставляем индивидуальное обучение, помощь и советы по классовой механике.
<br>
<b>Календарь и ивенты</b><br>
«Синие Ходоки» проводит массу ачив-ранов, испытаний, актуальных рейдов всех уровней сложности и т.д. Обычный день бойца начинается с открытия игрового календаря, который забит ежедневными событиями - остается только спланировать свое личное время и подписаться на интересующие походы.<br>
Также в гильдии практикуются ивенты и конкурсы, которые мы стараемся сделать доступными для игроков любого илвл и направления - это отличная возможность приятно провести время, отдохнуть, выцепить немножко ролплея и получить за все это призы.

