Став Синим Ходоком однажды, пути назад нет! Мы верны себе, своим товарищам, чтим свою историю и делаем все для нашего процветания. За своих братьев мы готовы порвать, за честь мы можем постоять. Если вы вступаете к нам, вы должны осозновать, что тут собрались игроки, для которых гильдия - это святое, а не просто надпись над бошкой персонажа. Другой такой гильдии найти не получится, поэтому мы за нее крепко держимся. Такого же отношения потребуют от вас.<br>
<br>
Наша гильдия не для неженок. Синие Ходоки - это воины севера, а не ясельная группа.У нас принято критиковать и указывать на ошибки. Ничего не имеем против дружбы и жвачки, но мы не разводим сопли, не сюсюкаемся с каждым игроком и поддерживаем дисциплину в мероприятиях и статиках. Для четкого понимания регламента общения внутри гильдии, читайте <a href="#news2" class="some_link">правила</a>.<br>
Что касается внешней политики, мы также НЕ позиционируем себя как "воинов света и добра". Мы жестоки, мы упрямы, мы преследуем СВОЮ выгоду. И нам всем глубоко насрать, когда у кого-то пригорает от того, что мы не имитируем излишнюю теплоту и любовь по отношению к игрокам внутри гильдии и за ее пределами.
<br>
У гильдии есть четкая иерархия. Звания не выдаются от балды, и вам не нужно переплетаться порочными связями с ГМом или офицерами, чтобы продвинуться по карьерной лестнице. Игроки повышаются в соответствии с должностью, занимаемой в гильдии, которая в свою очередь подразумевает обязанности, возлагаемые на игрока. То есть, например, за срок отсидки в рядах гильдии вас тоже повышать не будут. Привилегии и статус выдаются тем, кто вкладывает свои силы и время в общее дело. С каждого по способностям, каждому - по труду. Подробнее о званиях, должностях и субординации можно почитать на этой странице в радзеле <a href="#news" class="some_link">Звания и карьера</a>.<br>
Что касается субординации, можно сказать, что в нашей гильдии конституционная монархия. Полной демократии нет и никогда не будет ни в одной гильдии (окончательное решение будет всегда за Генералом), но у нас гильдия - это не собственность ГМа. Глава Совета считается как с мнением как Рейдеров, так и рядовых игроков, каждый вправе оспаривать решения, каждый вправе высказывать свое мнение и каждый вправе выдвинуть свою кандидатуру на высокие посты в гильдии. У нас инициатива не наказуема, а поощряется.
