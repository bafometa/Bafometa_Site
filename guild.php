<h4>Карьера в гильдии</h4>
<span>Как получить повышение в звании?</span><br>
Новички часто задают этот вопрос (а в 99% случаев задают его те, кто ничего не делает). Дня начала необходимо запомнить, что повышение просто так не дается. Повышаются ТОЛЬКО те, кто чем-либо помогает гильдии и занимает в ней активную позицию!<br>
<span>Если ты активный игрок</span>, попроси вышестоящих руководителей гильдии поставить себе заметку, общайся в чате гильдии, принимай участие во всех ивентах, в которые подходишь по уровню экипировки и скиллу, будь отзывчивым - ходи с согильдейцами по данжикам, установи себе дискорд и познакомься с самыми активными игроками, зарегистрируйся на форуме и принимай участие в обсуждениях на форуме. Кароче, мелькай везде, где только можешь - станешь <span>Рядовым</span> или даже <span>Сержантом</span>.<br>
<span>Если есть скилл</span>. Вступи в гильдейские статики, информация о существующих всегда есть на форуме. Получишь обязанности их посещать и прогрессировать, но зато прилагается звание <span>Рейдер</span> и веселая компания коллег по цеху.<br>
<span>Если ты богат</span>. Вкладывайся материалами/голдой в банк гильдии. В зависимости от внушительности вкладов - рост по званию.<br>
<span>Если есть мозг и время</span>. Займись форумом гильдии, видеомейкингом, дизайном, пиши/переводи гайды и статьи, веди новости гильдии, анализируй логи статиков и т.д. - получишь звание от <span>Сержанта</span> до <span>Совета Гильдии</span>, в зависимости от своей активности и продуктивности в этом деле.<br>
<span>Если есть организационные способности</span>. Стань РЛ-ом. Проводи регулярные рейды, управляй народом - будешь расти вплоть до звания <span>Совета Гильдии</span>.<br>
Короче, в «Синих Ходоках» возможно занять любое звание вплоть до Главы (включительно) при желании и возможности помогать гильдии и развивать ее, а не сидеть на попе ровно.<br>
