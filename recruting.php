    <h2>Вступление в гильдию</h2>
</div>
<div id="zvan">
    <div id="zvan-text">
Гильдия «Синие Ходоки» не зацикливается на одном аспекте игры, и сочетает в себе и PvE,и PvP, и Ачив-раны.<br>
В связи с этим, мы принимаем в гильдию любых <span>игроков, которые разделяют наши взгляды и активно готовы проводить время с гильдией</span>.<br><br>

<img class="foto2" align="right" src="http://forumfiles.ru/files/0019/33/10/57311.png" height="280px"><h4>В каких игроках заинтересована гильдия?</h4>
<div id="r-list">
<ul>
   <li><span>Общительных</span>. Если вы - завсегдатый зеленого чатика, если поставить дискорд и навещать войс чат для вас - это норма, мы заинтересованы в вашем нахождении в гильдии. Такие игроки украшают игровой процесс.</li>
   <li><span>Активных</span>. Если вы готовы врываться на все гильдейские события, куда подходит ваш гир, проводить время в рейдах или на рбг/бг вместе с гильдией, вы нам подходите.</li>
   <li><span>Отзывчивых</span>. Если вы готовы бросить свои дела и помочь товарищам, то вы быстро у нас приживетесь и вскоре пойдете на повышение.</li>
   <li><span>Развивающихся</span>. Даже если вы полный нуб в игре, но обладаете желанием развиваться и улучшать игровые навыки, то мы окажем в этом помощь и возьмем в свои ряды.</li>
   <li><span>Работоспособных</span>. В гильдии можно взять на себя много разной организационной работы, которая не требует супер-скилла - таких игроков мы с радостью принимаем, помогаем освоиться в должности и быстро повышаем в звании.</li>
   <li><span>Самодостаточных</span>. Если вы готовы выступать спонсором расходников, вам будут рады.</li>
</ul>
</div>
<br><br>
<img class="foto2" align="left" src="http://sg.uploads.ru/whISU.png">
<div id="r-list2">
<h4>Какие игроки не нужны гильдии?</h4>
<ul>
   <li><span>Раздражающие</span>. Если игроки не могут найти общий язык с согильдейцами и раздражают большинство игроков в чате или в войсе, вскоре их исключают из наших рядов.</li>
   <li><span>Мезантропы</span>.  Игроки, которые большую часть своего прайм-тайма висят на маунте в афк и никогда не присоединяются к гильдейским ивентам, кикают при первом же долговременном отсутствии.</li>
   <li><span>Эгоисты-солоплееры</span>. Игроки, которые не следуют правилам гильдейской субординации и отказываются выручать товарищей, попадают под штрафные санкции и чаще всего покидают гильдию.</li>
   <li><span>Крабовагоны</span>. Игроки, которые не хотят прилагать усилий в игре и постоянно напрашиваются вагоном, не вкладываются в общее дело ничем и вызывают пожар в жопе РЛ-ов нарушением правил рейдов и прогулами, вскоре покидают гильдию.</li>
   <li><span>Нытики</span>. Игроки, которые истерично требуют у РЛ-ов развлечений, хлеба и зрелищ, навязываются в рейды, не удовлетворяющих их скиллу/гиру и постоянно ноют на эту тему (ну, и естественно, сами ничего не делают для коллектива при этом), исключаются при первом же писке, который можно характеризовать как негативный отзыв о гильдии.</li>
   <li><span>Бомжи и попрошайки</span>. Игроки, которые желают обогатиться за счет гильдии, вымогают у игроков или казначеев ресурсы, крафт или голд исключаются из гильдии после одного предупреждения.</li>
</ul></div>
<br><br>В общем, мы ищем адекватных самодостаточных игроков, которые готовы принимать участие в гильдейских событиях или организовывать свои. :)

