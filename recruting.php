<h2>Стать Лидером «Синих Ходоков»</h2>
</div>
<div id="zvan">
    <div id="zvan-text">
Гильдия «Синие Ходоки» обладает открытой политикой в отношении привлечения РейдЛидеров и Организаторов. <br>
Во-первых, у нас нет диктатуры, мы предпочитаем КОМАНДНЫЙ стиль управления гильдии - мы даем свободно развиваться РейдЛидерам в наших рядах и предоставляем выгодные условия. Во-вторых, в нашей гильдии есть четкая иерархия: звания не выдаются от балды, и вам не нужно переплетаться порочными связями с ГМом или офицерами, чтобы продвинуться по карьерной лестнице, что дает возможность любому согильдейцу проявить организационные или творческие способности на одной из должностей. <br>
Ниже мы подробно опишем, почему <b>РЛам выгоднее вступать в «Синие Ходоки», а не создавать свою гильдию</b>.<br>
<br>
<div id="zvan-zach">
<h4>Бонусы для РейдЛидеров и организаторов</h4><br>
<b>Консультации и обучение.</b> Те, кто не обладают большим опытом руководства и организационной деятельности, могут получить советы и помощь от опытных РЛов гильдии. Вы не будете брошены на произвол судьбы со своими обязанностями, вам могут подсказать, рассказать, поделиться опытом, а также помочь не только на словах, но и на деле. <br>
<b>Помощь с рекрутингом для РЛов.</b> Любой опытный РЛ знает, что самый большой гемморой - это рекрутинг и поиск игроков. В нашей гильдии у них будет возможность получать в этом помощь от других РЛов и от Рекрутеров, а также воспользоваться сайтом гильдии для набора игроков: это значительно упрощает работу по формированию статиков. <br>
<b>PvE и PvP прогресс.</b> У руководителей PvE и PvP статиков есть возможность получить помощь от более опытных игроков из вышестоящих статиков, а игроки с организационных должностей получают бесплатные паровозы в рейды и возможность получить маунтов с рейдовых босов. <br>
<b>Обеспечение за счет гильдии.</b> РЛам, игрокам их статиков, а также организаторам предоставляется возможность получать химию, еду, чанты, ремонт, камни и прочее-прочее за счет гильдии. <br>
<b>Свобода действий.</b> Мы не лезим в процесс работы РЛов и не навязываем им правила других РЛов: вы сами определяете правила поведения на своих ивентах и кого с собой брать. <br>
</div>

<h4>Требования к РейдЛидерам и организаторам</h4>
<span>Стабильность.</span> Нам нужны игроки, которые готовы выполнять свои обязанности регулярно и ответственно. РЛы, которые прогуливают собственное же события, или организаторы, которые появляются в игре раз в год, не получают бонусов. <br>
<span>Самостоятельность.</span>  Вы должны иметь организаторские способности и справляться с обязанностями. Мы помогаем, но мы не будем делать всю работу за вас. РЛы, которые, пользуясь положением, валят всю работу на гильдию и не хотят ничего делать, кроме как поорать в микрофон на готовый статик, и организаторы, не выполняющие свои обязанности, нам не нужны. <br>
(для РЛов) <span>Навыки рекрутинга и добора.</span> Опять же - мы помогаем, но и самому тоже придется в этом участвовать. РЛы, которые ноют, что не могут найти людей, отменяют свои события из-за недобора и валят весь рекрутинг на гильдию будут разжалованы. <br>
(для РЛов) <span>Использование календаря.</span>  У нас принято заблаговременно отмечать в нем события, проводимые РЛами. Игроки, которые периодически хаотично кричат в чате "всем прив, а гоу на бг!" - не РЛы и не получают карьерный рост. <br>
(для РЛов) <span>Помощь другим РЛам и оргизаниторам.</span> Не только гильдия вас обслуживает, но и вы должны быть готовы оказать помощь гильдии. РЛы, которые пользуются бонусами, но отказывают в помощи другим РЛам и организаторам без адекватной причины - это паразитирующий организм.<br>
<span>Использование форума и тимспика.</span> Общие средства связи - это важно, иначе мы не единая гильдия. РЛы, которые готовы существовать только на просторах Азерота или только скайпе, не могут числиться НАШИМИ РЛами, а организаторы, которые не выходят за рамки игрового чата, нам не помощники. <br>
<span>Субординация.</span> РЛы и организаторы должны понять и принять нашу иерархию и знать, какие аспекты выходят за рамки их компетенции и при каких обстоятельствах Лидеры направлений или ГМ могут вмешаться в их деятельность. РЛы, которые не признают авторитетов и не умеют работать в команде, а также организаторы, злоупотребляющие властью, исключаются из гильдии. 

<br><br>Если вы - ответственный игрок, обладающий организационными способностями и желанием работать в команде, мы ждем вас! :) Вы можете оставить заявку на форуме или задать вопросы в <a href="http://bluewallkers.topbb.ru/viewforum.php?id=2">Гостевой книге</a>

