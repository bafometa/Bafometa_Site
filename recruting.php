<b>Строгие правила рейдов.</b><br>
В cтатике очень строгая политика рейдов, которую все обязуются соблюдать. От всех новичков требуется внимательно прочесть <a href="http://bluewallkers.topbb.ru/viewtopic.php?id=18" target="_blank">ПРАВИЛА СТАТИКА</a>. Несоблюдение этих правил не остается безнаказанным без исключений!<br>
<br>
<b>Строгий Аттенданс.</b><br>
Особое внимание обращаем на правила посещения статика. В статике не прощаются прогулы и опоздания без предупреждения! 
РЛ не будет вас персонально загонять в рейд или приглашать в дискорд. Если вас нет в назначенное время без заранее известной адекватной причины - это прогул, за которым следует наказание.<br>
<br>
<b>Наличие замены.</b><br>
Не приживутся игроки, которые считают себя незаменимыми, эра десяток закончилась: нет замены - нет ростера. У нас ЛЮБОЕ тело может отправиться посидеть на замене, и это не должно быть проблемой. Более того, замена обязана быть на связи и на готове заменить игроков, а не гулять афк. <br>
<br>
<b>Атмосфера и цели.</b><br>
В статике поддерживается <span>серьезное отношение к рейдам</span> и руководствуются принципами, что <span>время РТ необходимо проводить ПРОДУКТИВНО</span>, поэтому нам не подходят игроки, которые, руководствуясь принципами "это всего лишь игра", мешают прогрессу, нарушают дисциплину и подводят команду.<br>
<span>Статик Морксид</span> ставит своей задачей прогресс, в рамках РТ есть жесткая критика игроков. Однако на РТ есть место общению, дружбе и отдыху.<br> 

