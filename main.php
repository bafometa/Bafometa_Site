<h2>Гильдия «Синие Ходоки»</h2>
Ходоки – наша семья, убежище, спасение от теней прошлого, <br>
место, где ты становишься новым человеком.<br/>
Мы сильны и упрямы. Орудиями нашими будут показуха и ремесло. <br/>
Девиз Ходоков – максимальная эффективность при минимальном риске.<br/>
Друзей у нас нет. Все прочие – враги или просто недостойны доверия.<br/>
Мы отрекаемся от ярлыков. Добро и зло – категории, не имеющие к нам отношения. <br/>
Мы просто делаем свое дело и стараемся быть лучшими в нем. Без моральных дилемм и соплей.
